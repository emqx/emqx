-module(emqx_bpapi_sup).

-behavior(supervisor).

%% API:
-export([start_link/0]).

%% behavior callbacks:
-export([init/1]).

%% internal exports:
-export([]).

-export_type([]).

%%================================================================================
%% Type declarations
%%================================================================================

%%================================================================================
%% API functions
%%================================================================================

-define(SUP, ?MODULE).

-spec start_link() -> supervisor:startlink_ret().
start_link() ->
    supervisor:start_link({local, ?SUP}, ?MODULE, []).

%%================================================================================
%% behavior callbacks
%%================================================================================

init([]) ->
    emqx_bpapi:start(),
    Children = [],
    SupFlags = #{
        strategy => one_for_one,
        intensity => 10,
        period => 10,
        auto_shutdown => never
    },
    {ok, {SupFlags, Children}}.

